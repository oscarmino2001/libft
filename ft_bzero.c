#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*v;
	size_t			i;

	v = (unsigned char *)s;
	i = 0;
	while (i < n)
	{
		v[i] = 0;
		i++;
	}
}
